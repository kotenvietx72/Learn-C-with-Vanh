using System;

namespace CS03
{
    class Program
    {
        static void Main(string[] args)
        {
            float a = 10; int b = 6;
            Console.WriteLine("a + b = {0}", a + b);
            Console.WriteLine("a - b = {0}", a - b);
            Console.WriteLine("a * b = {0}", a * b);
            Console.WriteLine("a / b = {0}", a / b);
            Console.WriteLine("a % b = {0}", a % b);
            Console.WriteLine("a + b * 2 = {0}", a + b * 2);
        }
    }
